# For Order wise
list1 = [11, 23, 45, 66, 77, 88, 29, 30]
list1.sort()
print(list1)

# For UnOrder wise in reverse
list1 = [11, 23, 45, 66, 77, 88, 29, 30]
list1.reverse()
print(list1)

# For Order wise in reverse and sort
list1 = [11, 23, 45, 66, 77, 88, 29, 30]

list1.sort()
list1.reverse()

print(list1)
