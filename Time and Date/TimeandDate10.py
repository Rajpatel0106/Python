import time
current = time.localtime(time.time())
print("Year:", current.tm_year)#Show Current Year
print("Month:", current.tm_mon)#Show Current Month
print("Date:", current.tm_year)#Show Current Year
print("Day:", current.tm_mday)#Show Current Month Day

print("Weekday:", current.tm_wday)#Show Current Year Week left
print("Year day:", current.tm_yday)#Show Current Year Days
print("Hour:", current.tm_hour)#Show Current Hour
print("Minutes:", current.tm_min)#Show Current Min
print("Seconds:", current.tm_sec)#Show Current Sec
